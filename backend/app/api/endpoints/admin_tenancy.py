@router.post("/devices/{device_id}/rotate-token", response_model=DeviceProvisionResponse)
def rotate_device_token(
    device_id: int,
    db: Session = Depends(get_db),
    _: User = Depends(require_vendor_admin),
):
    """
    Generate a new token for a device, invalidating the old one immediately.
    The raw token is returned once — update the client machine's backend/.env.
    """
    device = db.query(Device).filter(Device.id == device_id).first()
    if not device:
        raise HTTPException(status_code=status.HTTP_404_NOT_FOUND, detail="Device not found")

    raw_token = secrets.token_urlsafe(32)
    device.token_hash = hashlib.sha256(raw_token.encode()).hexdigest()
    device.updated_at = datetime.now(timezone.utc)
    db.commit()
    db.refresh(device)

    return DeviceProvisionResponse(
        id=device.id,
        organization_id=device.organization_id,
        branch_id=device.branch_id,
        device_uid=device.device_uid,
        deployment_uid=device.deployment_uid,
        organization_uid=device.organization.organization_uid,
        branch_uid=device.branch.branch_uid,
        name=device.name,
        status=device.status,
        created_at=device.created_at,
        raw_token=raw_token,
        env_block=_env_block(device, raw_token),
    )
