@router.post(
    "/organizations/{org_id}/branches/{branch_id}/devices",
    response_model=DeviceProvisionResponse,
    status_code=status.HTTP_201_CREATED,
)
def provision_device(
    org_id: int,
    branch_id: int,
    data: DeviceCreate,
    db: Session = Depends(get_db),
    _: User = Depends(require_vendor_admin),
):
    """
    Create a new device and return its raw sync token once.
    The token is never stored — if lost, use rotate-token to generate a new one.
    """
    org = db.query(Organization).filter(Organization.id == org_id).first()
    if not org:
        raise HTTPException(status_code=status.HTTP_404_NOT_FOUND, detail="Organization not found")
    branch = db.query(Branch).filter(Branch.id == branch_id, Branch.organization_id == org_id).first()
    if not branch:
        raise HTTPException(status_code=status.HTTP_404_NOT_FOUND, detail="Branch not found")

    raw_token = secrets.token_urlsafe(32)
    token_hash = hashlib.sha256(raw_token.encode()).hexdigest()
    device_uid = str(uuid.uuid4())
    deployment_uid = (
        canonical_uuid(data.deployment_uid)
        if data.deployment_uid
        else str(uuid.uuid4())
    )

    device = Device(
        organization_id=org_id,
        branch_id=branch_id,
        device_uid=device_uid,
        deployment_uid=deployment_uid,
        name=data.name,
        token_hash=token_hash,
        status=DeviceStatus.ACTIVE,
    )
    db.add(device)
    db.commit()
    db.refresh(device)

    return DeviceProvisionResponse(
        id=device.id,
        organization_id=device.organization_id,
        branch_id=device.branch_id,
        device_uid=device.device_uid,
        deployment_uid=device.deployment_uid,
        organization_uid=device.organization.organization_uid,
        branch_uid=device.branch.branch_uid,
        name=device.name,
        status=device.status,
        created_at=device.created_at,
        raw_token=raw_token,
        env_block=_env_block(device, raw_token),
    )


